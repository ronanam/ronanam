package com.ktds.bycicle.vo;

public class BycicleVO {
	private int advancedQuantity;
	private int middleQuantity;
	private int basicQuantity;
	private int money;
	
	
	
	public int getAdvancedQuantity() {
		return advancedQuantity;
	}
	public void setAdvancedQuantity(int advancedQuantity) {
		this.advancedQuantity = advancedQuantity;
	}
	public int getMiddleQuantity() {
		return middleQuantity;
	}
	public void setMiddleQuantity(int middleQuantity) {
		this.middleQuantity = middleQuantity;
	}
	public int getBasicQuantity() {
		return basicQuantity;
	}
	public void setBasicQuantity(int basicQuantity) {
		this.basicQuantity = basicQuantity;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	
	
	
	
	
}
